/**
 * Tipos de datos
 */

// numero---Number
console.log(27);
console.log(27.55);

// Texto -- string
console.log("David");

// Logicos -- Booleans
console.log(true);
console.log(false);

//-indefinido ---- undefined
console.log(undefined);

// Null
console.log(null);

/**
 * Tipos de datos estructurales
 */
// -- Object o objeto
console.log({ nombre: "David", edad: 27, fecha: null, esAlto: false });

//-- Tipo de dato de tipo lista---array
console.log(["JUNIOR", "COLOMBIA", 21, { NOMBRE: "LOLA" }]);
